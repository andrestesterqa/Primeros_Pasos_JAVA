
public class manipula_cadenas2 {

	public static void main(String[] args) {
		
		String alumno1, alumno2;
		
		alumno1 = "David";
		alumno2 = "David";
		
		System.out.println(alumno1.equals(alumno2)); //Método para comparar el valor de 2 variables
		
		String alumno3, alumno4;
		
		alumno3 = "Linda";
		alumno4 = "linda";
		
		System.out.println(alumno3.equalsIgnoreCase(alumno4)); //Metodo para ignorar las mayúsculas
		
	}

}
